function updateLamps() {
  const lamp = document.getElementById("lampIndicator");
  const isOn = lampPositions.some(pos =>
    pos.x === playerPosition.x && pos.y === playerPosition.y
  );
  lamp.classList.toggle("lamp-yellow", isOn);
}

function movePlayer(dx, dy) {
  const newX = playerPosition.x + dx;
  const newY = playerPosition.y + dy;

  if (
    newX >= 0 && newX < 48 &&
    newY >= 0 && newY < 48 &&
    mazeLayout[newY][newX] === 0
  ) {
    playerPosition = { x: newX, y: newY };
    moveCount++;

    if (moveCount > 63) {
      alert("戻されてしまった……");
      location.reload();
    }
  }
  else {
    // 壁に衝突 → ランプを赤く点灯
    flashWallLamp();
    checkPosition();
  }
  updateLamps();
}


document.addEventListener("keydown", (e) => {
  switch (e.key) {
    case "ArrowUp":
      movePlayer(0, -1);
      break;
    case "ArrowDown":
      movePlayer(0, 1);
      break;
    case "ArrowLeft":
      movePlayer(-1, 0);
      break;
    case "ArrowRight":
      movePlayer(1, 0);
      break;
  }
});

function flashWallLamp() {
  const lamp = document.getElementById("wallLamp");
  lamp.classList.add("lamp-red");

  setTimeout(() => {
    lamp.classList.remove("lamp-red");
  }, 300); // 点灯時間（ms）
}

function checkPosition() {
  // 例えば X=2, Y=3 の場所で画像を表示
  if (playerPosition.x === 26 && playerPosition.y === 21) {
    document.getElementById('special-image').style.display = 'block';
  } else {
    document.getElementById('special-image').style.display = 'none';
  }
}
